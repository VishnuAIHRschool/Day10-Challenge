# Number (integer / Float) data type
# String ("Asvika")
# List (["Asvika", "Karthika", "Vishnu"])
# Dictionary ({"Name": "Asvika", "Age": 20})
# tuple (("Asvika", "Karthika", "Vishnu")) immutable
# set ({"Asvika", "Karthika", "Vishnu"})
# boolean (True / False)

# Number (Integer and Float)
age = 20
height = 5.8

# String
name = "Asvika"

# List
friends = ["Asvika", "Karthika", "Vishnu"]

# Dictionary
student = {
    "Name": "Asvika",
    "Age": 20
}

# Tuple (Immutable)
subjects = ("Maths", "Science", "English")

# Set (Stores unique values)
colors = {"Red", "Blue", "Green"}

# Boolean
is_student = True

# Printing values and their data types
print("Integer Value:", age)
print("Data Type:", type(age))
print()

print("Float Value:", height)
print("Data Type:", type(height))
print()

print("String Value:", name)
print("Data Type:", type(name))
print()

print("List Value:", friends)
print("Data Type:", type(friends))
print()

print("Dictionary Value:", student)
print("Data Type:", type(student))
print()

print("Tuple Value:", subjects)
print("Data Type:", type(subjects))
print()

print("Set Value:", colors)
print("Data Type:", type(colors))
print()

print("Boolean Value:", is_student)
print("Data Type:", type(is_student))